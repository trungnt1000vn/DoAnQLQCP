    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = techusername.getText();
        String password = new String(techpassword.getPassword());
        StringBuilder sb =new StringBuilder();
        if(username.equals("")){
            sb.append("Vui lòng nhập tài khoản \n");
        }
        if(password.equals("")){
            sb.append("Vui lòng nhập mật khẩu \n");
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this,sb.toString(),"Invalidation",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
        try{
            Connection connection = MyConnection.getMyConnection();
            String sql = "SELECT * FROM taikhoan WHERE UserName= ? AND Password = ?";
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1,techusername.getText());
            ps.setString(2,password);
            ResultSet rs =ps.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"Đăng nhập thành công");
                    jf_TrangChu tc = new jf_TrangChu(techusername.getText());
                    tc.setVisible(true);
                    this.dispose();
                }
             else {
                        JOptionPane.showMessageDialog(this,"Tài khoản hoặc mật khẩu không đúng!");
                 }
        
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed
    }
