    private void btnDelete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelete1ActionPerformed
        // TODO add your handling code here:
            int row = tableThucDon.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn món muốn xóa!", "Error Warning", JOptionPane.ERROR_MESSAGE);
            } else {
                int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    int mamon = Integer.valueOf(tableThucDon.getValueAt(row, 0).toString());
                    ThucDon thucdon = thucdonDAO.getTDByMaMon(mamon);
                    thucdon.setTrangthai("0");
                    thucdonDAO.deleteThucDon(thucdon);

                    LamMoi1();
                }
            }
    }//GEN-LAST:event_btnDelete1ActionPerformed

    private void btnEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdit1ActionPerformed
        // TODO add your handling code here:
        try {
            int mamon = Integer.valueOf(lblMamon.getText());
            if (txtTenmon.getText().equals("") || txtDvt.getText().equals("")
                    || txtDongia.getText().equals("") || txtMaNhom.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin!");
            } else if (thucdonDAO.getTDByMaMon(mamon) == null) {
                JOptionPane.showMessageDialog(this, "Món ăn không tồn tại!", "Error Warning", JOptionPane.ERROR_MESSAGE);
            } else {
                ThucDon thucdon = thucdonDAO.getTDByMaMon(mamon);
                thucdon.setTenmon(txtTenmon.getText());
                thucdon.setManhom(Integer.valueOf(txtMaNhom.getText()));
                thucdon.setDvtinh(txtDvt.getText());
                thucdon.setDongia(Float.valueOf(txtDongia.getText()));

                thucdonDAO.updateThucDon(thucdon);
                JOptionPane.showMessageDialog(this, "Cập nhật thành công!!");

                TableThucDon();;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn món cần sửa!");
        }
    }//GEN-LAST:event_btnEdit1ActionPerformed

