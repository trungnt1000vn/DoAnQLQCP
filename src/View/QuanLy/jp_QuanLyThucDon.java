    private void setDataThucDonTable(List<ThucDon> ThucDonList) {
        for (ThucDon thucdon : ThucDonList) {
            ModelThucDon.addRow(new Object[]{
                thucdon.getMamon(), thucdon.getManhom(), thucdon.getTenmon(),
                thucdon.getDvtinh(), String.format("%,.0f", thucdon.getDongia())
            });
        }

    }

